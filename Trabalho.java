    private void pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarActionPerformed
        String parametro = varivaellike.getText();
        Connection conn = new ConnectionFactory().getConnection();
      String sql = "select * from funcionario a\n" +
"inner join Cargos b on a.cod_cargo = b.cod_cargo\n" +
"where a.nome like'%"+parametro+"%'";
      try{
          PreparedStatement stmt = conn.prepareCall(sql);
          ResultSet result = stmt.executeQuery();

          while(result.next()) {
              
              String nome = result.getString("nome");
              String salarios = result.getString("salario");
              String cargoss = result.getString("des_cargo");
             retornoNome.setText(nome);
             salario.setText(salarios);
             cargo.setText(cargoss);
             
           
          }
        
          
      }catch(SQLException e) {
          e.printStackTrace();
      }
    }//GEN-LAST:event_pesquisarActionPerformed

